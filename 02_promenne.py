"""
LEKCE 2: Proměnné – škatulky na věci
======================================
Proměnná je jako pojmenovaná škatulka, do které něco uložíš.
"""

jmeno = "Míša"
vek = 10
oblibene_zvire = "pes"

print("Jmenuji se", jmeno)
print("Je mi", vek, "let.")
print("Moje oblíbené zvíře je", oblibene_zvire)

# f-string: hezčí způsob jak mísit text a proměnné
print(f"Ahoj! Jsem {jmeno} a je mi {vek} let.")

